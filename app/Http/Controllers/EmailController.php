    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Mail::raw(
            "Prezado, você recebeu uma solicitação de contato através do site.
            Solicitante: $request->nome
            Telefone: $request->telefone
            E-mail: $request->email
            Mensagem: $request->mensagem", function ($message) {

            $message->subject('Solicitação de contato no site');
            $message->from('contato@dmetzer.com.br', 'Contato site');
            $message->to('contato@dmetzer.com.br', 'Contato site');

            $message->getSwiftMessage();
        });

        return redirect()->back()->with('status', 'Solicitação de contato enviada!');
    }

